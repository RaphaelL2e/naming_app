module.exports = {
  poeticNames,
  wuxingEnergy,
  
  // 获取诗意名字
  getPoeticNames(count = 5) {
    const keys = Object.keys(poeticNames).sort(() => Math.random() - 0.5);
    return keys.slice(0, count).map(key => ({
      name: key,
      meaning: poeticNames[key]
    }));
  },
  
  // 根据五行推荐
  getByWuxing(element, count = 5) {
    const names = wuxingEnergy[element] || [];
    return names.slice(0, count).map(name => ({
      name,
      element,
      meaning: '五行属' + element
    }));
  }
};